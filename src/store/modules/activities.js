const getters = {
  isActPass: (state) => (actCondition) => {
    if (actCondition === 'all') {
      return state.ctInfors
    } else if (actCondition === '未完成') {
      return state.ctInfors.filter(activity => activity.actCondition !== '已结束')
    } else {
      actCondition = actCondition.split(',')
      return state.ctInfors.filter(activity => actCondition.indexOf(activity.actCondition) >= 0)
    }
  }
  // isActPass: (state) => state.ctInfors
}

const mutations = {
  /**
   * 批量改变“状态”(actCondition)
   * changeCondition: String // 修改后的服务状态
   */
  changeSomeActCondition: (state, changeCondition) => {
    for (let i = 0; i < state.ctInfors.length; i++) {
      for (let j = 0; j < state.tableChecked.length; j++) {
        if (state.ctInfors[i].actId === state.tableChecked[j]) {
          state.ctInfors[i].actCondition = changeCondition
        }
      }
    }
  },

  /**
   * 修改单个“状态”
   * change: {
   *   changeCondition: String //修改后事件的“状态”
   *   actId: Number //被修改事件的内容的id
   * }
   */
  changeOneActCondition: (state, change) => {
    for (let i = 0; i < state.ctInfors.length; i++) {
      if (state.ctInfors[i].actId === change.actId) {
        state.ctInfors[i].actCondition = change.changeCondition
      }
    }
  },

  /**
   * 多选内容至state
   */
  reserveChecked: (state, Checked) => {
    state.tableChecked = Checked
  }
}

